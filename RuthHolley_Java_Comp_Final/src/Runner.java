//Create the constructor for the runner class
public class Runner {
//Instance Variables 
	private String runnersName;
	private int runnersSpeed;
	private int restPercentage;

	//Gets and sets for all Instance Variables	
	
	public String getRunnerName() {
		return runnersName;
	}
	
	public void setRunnerName(String runnerName) {
		this.runnersName = runnerName;
	}
	
	public int getRunnerSpeed() {
		return runnersSpeed;
	}
	
	public void setRunnerSpeed(int speed) {
		this.runnersSpeed = speed;
	}
	
	public int getRestPercentage() {
		return restPercentage;
	}
	
	public void setRestPercentage(int restPercentage) {
		this.restPercentage = restPercentage;
	}	

}
